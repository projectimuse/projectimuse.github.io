        Our 2008 North American campus tour program involves visiting several North American universities in order to host
        conferences whose main aim is to increase the objectivity and complexity of North American students&#39;
        understanding of China. Designed to offer a variety of perspectives on issues in the foreign relations, government, economy, social
        structure, and popular culture of the country, these conferences encompass a number of events, including :
        <br><br>
        - panel discussions among student delegates from Peking and Tsinghua Universities,
        <br>
        - meetings with student leaders, and journalists, professors, and other experts on China,
        <br>
        - talks and question-and-answer sessions with prominent speakers,
        <br>
        - a photo exhibition on modern China sponsored by Kodak,
        <br>
        - screenings of documentaries and popular movies,
        <br>
        - performances and cultural workshops, and catered social events.
