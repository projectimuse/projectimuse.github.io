	<h3>Landscapes of Modern China</h3>
	
	The IMUSE 2008 "Landscapes of Modern China" photo exhibition was displayed at each
	of the schools visited on our North American campus tour. Prepared and donated by our
	sponsors at Kodak, the gallery consisted of twelve separate square-meter panels and
	included more than 90 photographic depictions by <i>National Geographic</i> of the beautiful
	natural features, pastoral rural communities, stirring urban scenes, and daily lives
	of students and other inhabitants of China.<br><br>


	  <h3>Panel Discussion</h3>
      Panel  discussions included questions like the following:<br>
	<p>
      <ul>
        <li>In your  opinion, what are some of the most frustrating misconceptions that Americans  have about China? What are the three most frustrating misconceptions that  Chinese people have about America? What can be done to rectify these  misconceptions?</li><br>
		<img class="imgr" src="../../images/programs/2008/panel.jpg" style="width: 300px; height: 180px;">
        <li>According to  a 2007 <i>Newsweek</i> poll, about 90% of Americans say that they identify with  a specific religion. Conversely, in China, the US Department of State reports  that most of the country does not follow any organized religion-adherents to  Buddhism, the most popular religion, represent only 8% of the population.  Should we take these statistics to mean that Chinese people are less spiritual  than Americans? From what sources do Chinese people derive their ethical  ideals? How might this cultural difference impact relations between Americans  and Chinese?</li><br>	
		
        <li>Celebrities  like Jet Li, Jackie Chan, and Zhang Ziyi have achieved widespread popularity in  America. At the same time, many American personalities have also gained  celebrity status in China. Do what extent do you think each country's cultural  exports accurately represent their respective societies?</li>
      </ul>
	  </p>
      Our panel  discussions were among the most popular events at our conferences and garnered  a collective audience of more than 3,000 students.
