      Each campus visit included at least one celebrated speaker to talk about international relations between China and the United States, as well as at least one publicly open discussion among local student leaders, Chinese student delegates, and experts on China to discuss topics chosen in a survey of North American tertiary school students. <br><br>
      Fourteen student delegates were selected from a large and extremely competitive pool of over 600 candidates at Peking and Tsinghua Universities to participate in the campus tour panel discussions, speaker events, and socials with the students of each school visited during the program. They represent a wide range of interests and  educational specializations and offered a rich collection of perspectives to the students with whom they interacted during the campus tour. <br><br>
	 
