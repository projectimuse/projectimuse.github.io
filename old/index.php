<h3>Our Mission</h3>
    <br>Founded in 2008, Initiating Mutual Understanding through Student Exchange (IMUSE) is a collaboration between Harvard University, Peking University, and Tsinghua University students, aiming to foster mutual curiosity, respect, and understanding between people in China, Europe, and the United States by providing top college and graduate students from these regions an intimate forum for discussion and deep personal reflection 
    about the key issues affecting China and the world today.
  <br><br>IMUSE has three primary goals:

<br><br>- To create socially intimate and intellectually meaningful face-to-face interactions among a small group of 
promising American and Chinese university students to increase mutual curiosity, trust, and understanding among them;

<br><br>- To develop an alumni network to facilitate high-impact, positive social change through the continued collaboration 
of former participants of IMUSE;

<br><br>- To maintain a strong partnership among IMUSE branches at Harvard, Peking, and Tsinghua Universities through fruitful 
international exchanges among board members;

<br><br>We strive to achieve these goals with our biggest event, an annual 10-day conference in Beijing, bringing together top students from the United States, Europe, and China,
followed by an opportunity to reflect on the experience with creative essays and photography competitions. 
