    <h3>&ldquo;China in my Eyes&rdquo; Essay and Photography Competition </h3>
	<p>
        Since North American Campus Tour events were limited to select schools, IMUSE also designed the "China in My Eyes" essay and photography competition, open to all North American postsecondary school students. The competition encouraged its entrants to carefully examine their own views about China and convey their ideas through original essays or photographs. The six grand-prize winners of the essay and photography competitions received either a new Lenovo laptop or a spot in our Beijing Olympic Summer Fellowship Program. The twenty runners-up each received a set of five officially licensed Beijing Olympic souvenir coins (global circulation: 60,000). All competition winners received personalized certificates and publication of their winning entries on our website.<br><br>
		<a href="essays.php">Click Here</a> to read winning essays<br>
		<a href="photog.php">Click Here</a> to view winning photographs
    </p>
    <h3>&ldquo;My Top Ten&rdquo; Survey of Important Issues Facing Modern China </h3>
	<p>
        From winter 2007 to spring 2008, the IMUSE website hosted a survey regarding various issues being addressed by China today, from environmental pollution and governmental corruption to human rights and religious freedom. North American postsecondary school students were eligible to select ten of twenty choices that they felt demanded the most urgent attention. The survey ultimately had over 1200 responses from hundreds of different schools, and the results were used to design the questions for our North American Campus Tour panel discussions.
		<br><br>
		<a href="topten.php">Click Here</a> to view the randomly selected survey winners who received a spot at the 2008 Beijing Fellowship Program or Beijing Olympic souvenirs.
    </p>
    <h3>Beijing Olympic Summer Fellowship Program </h3>
	<p>
        Three of our five grand-prize &ldquo;China in my Eyes&rdquo; competition winners and our &ldquo;My Top Ten&rdquo; survey winner attended the 2008 Beijing Olympic Summer Fellowship Program in Beijing during the first two weeks of August 2008. The purpose of the fellowship was to broaden the North American students&rsquo; perspective on China by bringing them not just to traditional tourist sites but also to places of historical and contemporary significance. Each fellow was assigned a Chinese student liaison to help them with language and culture differences as well as deepen and personalize the exchange experience. In addition to visiting a number of cultural sites, attending Olympic events, and participating in academic lectures and conversation tables, the fellows and liaisons also held a public panel discussion at Peking University similar to those hosted by our fourteen Chinese delegates during the North American Campus Tour Program.<br><br>
