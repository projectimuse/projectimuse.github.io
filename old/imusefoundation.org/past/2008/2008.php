
  <h2>2008 Overview</h2>
    <div id="content">
        IMUSE kicked off its inaugural year with four programs designed to bring an objective and affirmative introduction of the Beijing Olympics and modern China to as wide an audience of North American students as possible:<br><br>

    <h3>North American Campus Tour Program</h3>
    <p>
        In the winter of 2007, IMUSE selected fifteen student delegates from over 600 applicants at Tsinghua and Peking Universities to visit eleven North American universities in the spring of 2008. At each institution, the delegates engaged journalists, academics, and local student leaders before a local audience in moderated panel discussions about a number of different topics concerning China, such as its increasingly influential role in the global marketplace, its slowly westernizing culture, the balance between its environmental consciousness and its economic growth, and more. Specific topics of discussion were based on the results of the &ldquo;My Top Ten&rdquo; poll posted on our website last winter and spring, which received over 1200 responses from hundreds of different schools.<br><br>
        In concert with these panel discussions, IMUSE offered other on-campus events at each university, including talks by prominent guest speakers from Dr. David Kang to Mr. Jet Li, documentary screenings, &ldquo;Landscapes of Modern China,&rdquo; a professional photo exhibition prepared by Kodak, and a variety of school-specific workshops, performances, and other activities.<br><br>
