<h1>About IMUSE</h1>

This Chinese-American fellowship was founded in 2007 by Harvard University, Peking University, and Tsinghua University. This program recruits students from all over the world in order to facilitate understanding through cross cultral communication and friendships. IMUSE is currently one of the most influential, student-run exchange programs in China. <br><br>

From 2008-2013, IMUSE has successfully hosted six consecutive fellowships. Every summer, American students travel hundreds of miles to meet up with Chinese students in Beijing to participate in a two-week exchange program. In the last six years, IMUSE has inspired students to explore Sino-American relations in a global context. From the 2008 tour of North American Universities, the 2011 University of Oxford-Chinese Conference, and the 2012-2013 exchanges to Hong Kong, it is evident that IMUSE participants are not only delegates of their respective countries, but also global citizens and future leaders in our shrinking world. <br><br>

Over the years,  IMUSE has always stayed true to its original vision of helping students embrace our globalized world. It has provided students with an opportunity to experience cultural differences, gain insight into new perspectives, share innovative ideas, and to work together to comprehend the changes in our complex world. Because of this, IMUSE's program has been succeeded year after year, by extraordinary delegates, dedicated staff, and a large community of IMUSE supporters.<br><br>

IMUSE is sponsored by the Harvard IMUSE Team, Tsinghua University- Association of Student International Communication (ASIC), Peking University- Student International Communication Association (SICA), and China Young Leaders Foundation (CYLDS)


