The theme of this year's IMUSE conference will be a combination of economics, art, and public welfare (including non-profts, NGOs, community service, volunteering, etc.). Instead of only focusing discussions on Sino-American economic relations, we hope to use art and public welfare as another pathway to understand the cultural differences between China and the United States. Similar to the 2012 and 2013 IMUSE conference, this year will also have two parts, one in Beijing and one in Hong Kong. <br><br>

<h3>Beijing Itinerary</h3>

<strong>Forum on Chinese Development:</strong><br><br>
The IMUSE Forum on Chinese Development will feature a series of lectures from different scholars, businessmen, and political figures. IMUSE delegates will not only be able to participate in these lectures, but will also have an opportunity to engage in discussion, debate with each other, and converse directly with these speakers. <br><br>

<strong>Public Welfare Theme:</strong><br><br>
Through volunteering, the delegates will have the opportunity to interact with the lower rungs of Chinese society and understand how the Chinese view public welfare, community service, and charity work. IMUSE delegates will also have the chance to participate in discussions with PEER, IJION, and other organizations that have contributed greatly to public welfare in China. Participants will gain a better understanding of public welfare in China and the challenges to continue its development.<br><br>

<strong>Economic Theme:</strong><br><br>
Apart from engaging in discussions about the most pressing Sino-America economic issues, Chinese and American delegates will also participate in a case-study competition at one of the most prestigious consulting firms in China. This experience will not only allow Chinese and American students to work closely together but will also expand the delegates' international scope and understanding of each others' cultures.<br><br>

<strong>Art Theme:</strong><br><br>
In our “exploration of Beijing” activity, delegates will have the opportunity to create their own personalized, one-day tour around Beijing. Delegates will have the opportunity to explore all the different types of art that are found throughout Beijing. In addition to this, students will also have the chance to work together and create their own art.<br><br>


<h3>Hong Kong Itinerary</h3>
With the support of the China Young Leaders Foundation (CYLFS), the IMUSE-Hong Kong exchange will be focused on encountering cultural differences (especially within China), understanding complex social issues, and appreciating different types of art. Here is a brief list of some of the activities already planned for Hong Kong.<br><br>
